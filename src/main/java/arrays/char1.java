package arrays;

public class char1 
{
	public static void main(String[] args) 
	{
		//declaring array
		char ch[] = new char[4]; //4 is the size of array
		
		//initializing the aray
		ch[0] = 'a';
		ch[1] = 'b';
		ch[2] = 'c';
		ch[3] = 'd';
		
		//using array
		System.out.print(ch[0]+" ");
		System.out.print(ch[1]+" ");
		System.out.print(ch[2]+" ");
		System.out.print(ch[3]);		
	}

}
